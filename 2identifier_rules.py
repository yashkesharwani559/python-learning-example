# identifier rules in python
# can have alphabets, digits and underscores [0-9a-zA-Z_]
# but it should start with an alphabet
# identifiers can't start with a digit or underscore
# there can be no space allowed 
# dollar are not allowed to use in identifier in python

a = 34
aa = 435

harry = 455

sameer = 54

_sameer = 23 # not allowed

