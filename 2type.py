# type() function and typecasting

a = 34

print(type(a))

b = 2.45

print(type(b))

c = "hello"

print(type(c))

d = "12"

print(type(d))

print(type(float(d)))

print(type(int(d)))

print(d)

# typeconversion
# float() string/integer to float
# int() string/float to integer
# str() integer/float to string

print(type(float(3)))


